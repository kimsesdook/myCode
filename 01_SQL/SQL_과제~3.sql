--1.학생이름과 주소지를 표시하시오. 정렬은 오름차순을
SELECT STUDENT_NAME AS "학생 이름", STUDENT_ADDRESS AS "주소지"
FROM TB_STUDENT
ORDER BY STUDENT_NAME ASC;
--2. 휴학중인 학생들의 이름과 주민번호를 나이가 적은 순서로 화면에 출력
SELECT STUDENT_NAME,STUDENT_SSN
FROM TB_STUDENT
WHERE ABSENCE_YN = 'Y'
ORDER BY STUDENT_SSN DESC;
--3 주소지가 강원도나 경기도인 학생들 중 1900년대 학벌을 가진 학생들의 이름과 학번, 주소를 이름의 오름차순 출력
SELECT STUDENT_NAME AS" 학생이름", STUDENT_NO AS "학번", STUDENT_ADDRESS AS "거주지주소",ENTRANCE_DATE
FROM TB_STUDENT
WHERE (STUDENT_ADDRESS LIKE '경기도%' OR STUDENT_ADDRESS LIKE '강원%' )
AND TO_NUMBER(EXTRACT(YEAR FROM TO_DATE((ENTRANCE_DATE)))) < 2000
ORDER BY STUDENT_NAME;

--4 현재 법학과 교수 중 가장 나이가 많은 사람부터 이름을 확인할 수 있는 SQL문장 작성
