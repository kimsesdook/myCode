--1.D영어영문학과 SQL작성
SELECT STUDENT_NO AS 학번, STUDENT_NAME AS 이름, TO_DATE(ENTRANCE_DATE) AS 입학년도
FROM TB_STUDENT
JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
WHERE DEPARTMENT_NO =002
ORDER BY 입학년도 ASC;

--2춘 기술대학교 교수중 이름이 세글자가 아닌 교수의 신상
SELECT PROFESSOR_NAME , PROFESSOR_SSN
FROM TB_PROFESSOR
WHERE MOD(LENGTH(PROFESSOR_NAME),2)=0;
--3.춘 기술대학교 남자 교수들의 이름과 나이를 출력하는 SQL문장. 단 나이가 적은 사람에서 많은 순서로 화면 출력
--단 교수중 2000년 이후 출생자는 없음
SELECT PROFESSOR_NAME 교수이름, EXTRACT(YEAR FROM SYSDATE) - (1900+SUBSTR(PROFESSOR_SSN, 1, 2)) 나이
FROM TB_PROFESSOR
WHERE SUBSTR(PROFESSOR_SSN, 8, 1) = 1
ORDER BY 나이;
--4.교수들의 이름 중 성을 제외한 이름만 출력하는 문장 작성. 출력헤더는 이름으러
SELECT SUBSTR(PROFESSOR_NAME,2,2)AS 이름
FROM TB_PROFESSOR;
--5.춘 기술대학교의 재수생 입학자를 구하려고 한다. 이때 19살에 입학하면 재수를 하지 않은 것으로 간주
SELECT STUDENT_NO, STUDENT_NAME
FROM TB_STUDENT
WHERE EXTRACT(YEAR FROM TO_DATE(ENTRANCE_DATE)) - EXTRACT(YEAR FROM TO_DATE(SUBSTR(STUDENT_SSN,1,6)))!=19 
AND EXTRACT(YEAR FROM TO_DATE(ENTRANCE_DATE)) - EXTRACT(YEAR FROM TO_DATE(SUBSTR(STUDENT_SSN,1,6))) >19;

