--8 춘 기술대학교의 2000년 이후 입학자들은 학번이 A로 시작하게 되어있다. 2000년도 이전 학번을 받은 학생들의 학번과 이름을 보여주라
SELECT STUDENT_NO,STUDENT_NAME
FROM TB_STUDENT
WHERE TO_NUMBER(EXTRACT (YEAR FROM(ENTRANCE_DATE))) < 2000;

--9.학번이 A517178인 한아름 학생의 학점 총 평점을 구하는 SQL문을 작성하시오. 단 이때 출력 화명의 헤더는 평점이라 하고
--점수는 반올림하여 소수점 이하 한자리만 표시
SELECT ROUND(SUM(POINT)/8,1)
FROM TB_GRADE
JOIN TB_STUDENT USING(STUDENT_NO)
WHERE STUDENT_NO = 'A517178';

--10.학과별 학생수를 구하여 '학과번호','학생수'의 형태로 헤더를 만들어 결과값이 출력되도록 하시오
SELECT DEPARTMENT_NO AS 학과번호, COUNT(DEPARTMENT_NO)
FROM TB_STUDENT
GROUP BY DEPARTMENT_NO
ORDER BY DEPARTMENT_NO;

--11지도 교수를 배정받지 못한 학생의 수를 알아내는 SQL문
SELECT COUNT(*)
FROM TB_STUDENT
WHERE COACH_PROFESSOR_NO IS NULL;
--12 학번이 A12113인 김고운 학생의 년도 별 평점을 구하는 SQL문 작성
SELECT SUBSTR(TERM_NO,1,4)AS "년도",ROUND(AVG(POINT),1)
FROM TB_GRADE
WHERE STUDENT_NO = 'A112113'
GROUP BY SUBSTR(TERM_NO,1,4);
--13 학과 별 휴학생 수를 파악
SELECT DEPARTMENT_NO, COUNT(*)
FROM TB_STUDENT
WHERE ABSENCE_YN = 'Y'
GROUP BY DEPARTMENT_NO
ORDER BY DEPARTMENT_NO;

--14 춘 대학교에 다니는 동명이인 학생들의 이름 찾기
SELECT STUDENT_NAME AS 동일이름, COUNT(STUDENT_NAME) AS 동명인수
FROM TB_STUDENT
GROUP BY STUDENT_NAME
HAVING COUNT(STUDENT_NAME)>1;

